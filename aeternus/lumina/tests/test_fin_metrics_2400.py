def sharpe_ratio(rets, rf=0.0):
    excess = np.array(rets) - rf/252
    if excess.std() < 1e-12: return 0.0
    return float(excess.mean() / excess.std() * 252**0.5)

def max_drawdown(rets):
    cum = np.cumprod(1 + np.array(rets))
    peak = np.maximum.accumulate(cum)
    return float(((cum - peak) / peak).min())

def win_rate(rets):
    a = np.array(rets)
    return float((a > 0).sum() / len(a)) if len(a) > 0 else 0.0

