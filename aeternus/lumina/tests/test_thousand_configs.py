def test_transformer_block_100(d, h, B, T, cls_name):
    import importlib
    mod = importlib.import_module('transformer')
    cls = getattr(mod, cls_name)
    if cls_name == 'GatedTransformerBlock':
        block = cls(d)
    else:
        block = cls(d, h)
    x = torch.randn(B, T, d)
    out = block(x)
    assert out.shape == (B, T, d)
    assert not torch.isnan(out).any()

